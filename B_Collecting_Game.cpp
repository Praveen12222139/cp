void solve() {
    int n;
    cin >> n;

    vector<int> A(n);
    for (int i = 0; i < n; i++) {
        cin >> A[i];
    }

    vector<int> B = A; 
    sort(B.begin(), B.end());  

    vector<long long> prefix(n);
    prefix[0] = B[0];
    for (int i = 1; i < n; i++) {
        prefix[i] = prefix[i - 1] + B[i];
    }

    unordered_map<int, int> map;
    map[B[n-1]] = n - 1;

    for (int i = n - 2; i >= 0; i--) {
        if (map.find(B[i]) != map.end()) continue;
        if (prefix[i] >= B[i+1]) map[B[i]] = map[B[i+1]];
        else map[B[i]] = i;
    }

    for (int a : A) {
        cout << map[a] << " ";
    }
    cout << endl;
}
int main() {

    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}